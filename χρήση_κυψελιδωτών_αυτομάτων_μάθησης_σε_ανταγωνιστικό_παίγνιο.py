for t in range(num_rounds):
    for cell in all_cells:
        cell.score = 0
    for i in range(num_cells):
        current_cell = all_cells[i]

        for offset in range(-neighborhood_size, neighborhood_size + 1):
            j = (i + offset) % num_cells
            if i != j:
                opponent_cell = all_cells[j]
                current_cell.play_and_learn(opponent_cell, my_idx=i, opponent_idx=j)
    current_round_dominant_strategies = []
    for cell in all_cells:
        dominant_strategy = max(cell.probabilities, key=cell.probabilities.get)
        current_round_dominant_strategies.append(dominant_strategy)
    dominant_strategy_history.append(current_round_dominant_strategies)
    counts_total = {'R': 0, 'S': 0, 'P': 0}
    counts_a = {'R': 0, 'S': 0, 'P': 0}
    counts_b = {'R': 0, 'S': 0, 'P': 0}
    for i, cell in enumerate(all_cells):
        dominant_strat = max(cell.probabilities, key=cell.probabilities.get)
        counts_total[dominant_strat] += 1
        if i < num_group_a: #Ομάδα Α
            counts_a[dominant_strat] += 1
        else: #Ομάδα Β
            counts_b[dominant_strat] += 1

    #Υπολογισμός ποσοστών
    for s in ['R', 'S', 'P']:
        strategy_distribution_history_total[s].append(counts_total[s] / num_cells)
        strategy_distribution_history_a[s].append(counts_a[s] / num_group_a if num_group_a > 0 else 0)
        strategy_distribution_history_b[s].append(counts_b[s] / num_group_b if num_group_b > 0 else 0)

