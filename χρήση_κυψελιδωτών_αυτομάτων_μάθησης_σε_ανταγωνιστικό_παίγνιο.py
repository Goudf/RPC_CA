import random
import matplotlib.pyplot as plt
import numpy as np
from collections import Counter
from matplotlib.colors import ListedColormap

class CAPlayer:
    def __init__(self, player_id, learning_rate=0.1):
        self.player_id = player_id
        self.strategies = ['R', 'S', 'P']
        # Αρχικές πιθανότητες για κάθε στρατηγική (ίσες)
        self.probabilities = {s: 1/len(self.strategies) for s in self.strategies}
        self.current_strategy = random.choice(self.strategies)
        self.score = 0
        self.learning_rate = learning_rate #Ρυθμός μάθησης
        self.opponent_memory = {}# Αποθηκεύουμε την τελευταία κίνηση που έκανε ο συγκεκριμένος αντίπαλος

    def choose_strategy(self, opponent_move_history=None):
        if opponent_move_history and self.player_id in opponent_move_history:
            last_opponent_move = opponent_move_history[self.player_id]['last_move']
            if last_opponent_move:
                winning_move = {'R': 'P', 'P': 'S', 'S': 'R'}[last_opponent_move] #Βρίσκουμε την κίνηση που κερδίζει την τελευταία κίνηση του αντιπάλου
                if self.probabilities[winning_move] > 0.6:
                    self.current_strategy = winning_move
                    return self.current_strategy

        self.current_strategy = random.choices(self.strategies, weights=[self.probabilities[s] for s in self.strategies], k=1)[0] #αλγόριθμος ανταμοιβής-ποινής
        return self.current_strategy

    def update_probabilities(self, played_strategy, result):
        if result == 'win':#Ανταμοιβή
            self.probabilities[played_strategy] += self.learning_rate * (1 - self.probabilities[played_strategy])
        elif result == 'lose':#Ποινή
            self.probabilities[played_strategy] -= self.learning_rate * self.probabilities[played_strategy]
        # Για ισοπαλία δεν αλλάζουμε την πιθανότητα

        #Διατηρήσουμε το άθροισμα στο 1
        total_prob = sum(self.probabilities.values())
        if total_prob != 0:
            adjustment_factor = 1.0 / total_prob
            for s in self.strategies:
                self.probabilities[s] *= adjustment_factor

        # Διασφάλιση ότι οι πιθανότητες δεν πέφτουν κάτω από ένα ελάχιστο όριο
        for s in self.strategies:
            self.probabilities[s] = max(0.001, min(0.999, self.probabilities[s]))

        #Κανονικοποίηση ακριβώς στο 1
        sum_probs = sum(self.probabilities.values())
        for s in self.strategies:
            self.probabilities[s] /= sum_probs

    def play_and_learn(self, opponent, my_idx, opponent_idx):
        opponent_move = opponent.choose_strategy()
        my_move = self.choose_strategy(opponent_move_history={opponent.player_id: {'last_move': opponent_move}})

        result = get_result(my_move, opponent_move)
        if result == 'win':
            self.score += 1
        elif result == 'lose':
            self.score -= 1

        #Ενημέρωση των πιθανοτήτων με βάση το αποτέλεσμα του παιχνιδιού
        self.update_probabilities(my_move, result)

        #Αποθήκευση της κίνησης του αντιπάλου
        self.opponent_memory[opponent.player_id] = {'last_move': opponent_move}

def get_result(move1, move2):
    wins = {'R': 'S', 'S': 'P', 'P': 'R'}
    if move1 == move2:
        return 'draw'
    elif wins[move1] == move2:
        return 'win'
    else:
        return 'lose'

num_cells = 50 #Συνολικός αριθμός κυψελών
num_rounds = 300 #Αριθμός βημάτων προσομοίωσης
neighborhood_size = 3# Μέγεθος γειτονιάς
num_group_a = num_cells // 2
num_group_b = num_cells - num_group_a

cells_a = [CAPlayer(player_id=f'A_{i}', learning_rate=0.03) for i in range(num_group_a)]# Ομάδα Α
cells_b = [CAPlayer(player_id=f'B_{i}', learning_rate=0.15) for i in range(num_group_b)]# Ομάδα B
all_cells = cells_a + cells_b
dominant_strategy_history = []
